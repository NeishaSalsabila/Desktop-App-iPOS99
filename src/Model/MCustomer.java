package Model;

/**
 *
 * @author MSI-PC
 */
public class MCustomer {
    private int id;
    private String nama, nik, no_hp;

    public MCustomer(int id, String nama, String nik, String no_hp) {
        this.id = id;
        this.nama = nama;
        this.nik = nik;
        this.no_hp = no_hp;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }

    public String getNo_hp() {
        return no_hp;
    }

    public void setNo_hp(String no_hp) {
        this.no_hp = no_hp;
    }
    
}
